     <p>UMT led and is among the very few universities which are heading online classes on Zoom successfully. In these dire times, the university ensured not to be held back by the circumstances but to overcome them and keep moving forth. Another such example was the PhD dissertation defense of Nabeel Sabir from the Computer Science Department which was held on April 23, 2020, via Zoom. The topic defended by Nabeel Sabir was “Bridging the Communication Barrier for the Deaf in Pakistan using Information Technology”.</p>
