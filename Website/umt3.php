<h1 style="font-size: x-large;">WE LEAD. OTHERS FOLLOW</h1>
<h2 style="font-size: medium ;">Thursday, March 19, 2020</h2>
<p style="font-size: medium;">School of Business and Economics, UMT among leading Asian business schools in 2020: Newsweek<br /><br />

American weekly magazine, Newsweek, has called the School of Business and Economics at UMT an emerging key player in the field of business studies, placing it at par with other top business schools of Pakistan.<br /><br />

As per the magazine, SBE is one of the leading business schools of Asia in 2020. Newsweek reports that SBE is ranked among the best business schools in the region with more than 70 faculty members, more than 27 of whom hold PhD degrees. The corporate world bears witness to the fact that SBE offers quality programs related to management sciences that are rigorous, and are globally recognized.<br /><br />

Newsweek further elucidates that the programs at SBE are geared towards equipping future leaders, professionals and executives with the knowledge, technology, skillset and expertise to make make a real difference in their respective organizations.
The global magazine also acknowledges SBE's efforts to become the best in Asia through key initiatives such as AMDIP, which SBE founded.<br /><br />

 SBE has successfully maintained its stature as a nationally and internationally recognized school that is acknowledged for its rich and diverse portfolio of learning, professional training, executive development, practical research, and consulting solutions.
The Newsweek report also sheds light on various acceditations held by SBE, including that from Association of Management Development in South Asia (AMDISA). SBE has been awarded W category for BBA and MBA programs with accreditation by the National Business Education Accreditation Council (NBEAC).<br /><br />

According to Newsweek, SBE is also an active member of the AACSB International, USA, and European Foundation for Management Development (EFMD) and is now focusing upon EPAS, an international program accreditation system operated by the EFMD. SBE has recently availed membership of the Alliance on Business Education and Scholarship for Tomorrow, a 21st Century Organization (ABEST21), an accrediting body based in Japan and is also a voting member at the Publishers International Linking Association (PILA).</p>
