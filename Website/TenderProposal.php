<p>Tender document may be obtained from the Office of Procurement or downloaded
 from the official website of the University, www.umt.edu.pk. The fee of tender
  document is non-refundable Rs.1000/- (Rupees one thousand only), which will be 
  accepted in the shape of pay order/demand draft/banker's cheque drawn in Lahore
   in the name of University of Management and Technology, Lahore. Interested bidders 
   should submit their bids along with 2% earnest money of the total quoted price in sealed 
   envelope of financial bid in the shape of CDR/pay order/demand draft/banker's cheque 
   drawn in Lahore in favor of University of Management and Technology, Lahore. Sealed 
   bids should reach to the Office of Procurement on or before Wednesday, May 20, 2020
    by 2:30 pm. No telephonic/telegraphic/faxed bid is acceptable. Bids received within
     the stipulated date and time will be opened by the Purchase Committee in the Meeting 
     Room of the University on Thursday, May 21, 2020 at 02:00 pm in the presence of
      the Bidders or their authorized representatives. UMT reserves the right to reject
       any/all bids or proposals at any stage without intimation.</p>
